CREATE TABLE Hotel(
HotelId varchar2(6) PRIMARY KEY,
HotelName varchar2(100),
StateName varchar2(50),
Address varchar2(100),
StarRating number(1),
SRoom Number(1),
DRoom Number(1),
FRoom Number(1)
);

CREATE TABLE Price(HID varchar2(6) REFERENCES Hotel(HotelId) ON DELETE CASCADE,
SPrice number(4),
DPrice number(4),
FPrice number(4),
PRIMARY KEY(HID)
);

CREATE TABLE Facility(HID varchar2(6) REFERENCES Hotel(HotelId) ON DELETE CASCADE,
FreeWifi number(1),
AirConditioning number(1),
CarPark number(1),
Pool number(1),
Spa number(1),
PetFriendly number(1),
Restaurant number(1),
PRIMARY KEY(HID));

CREATE TABLE Booking(RoomId VARCHAR2(9),
RoomType varchar2(6),
CheckIn Date,
CheckOut Date,
Price number(4),
CustomerId varchar2(10) PRIMARY KEY);

CREATE TABLE Customer(CID varchar2(10) REFERENCES Booking(CustomerId) On DELETE CASCADE,
CustomerName varchar2(20),
Gender varchar(6),
Age number(3),
PhoneNumber number(10),
PRIMARY KEY(CID));

